import usePlacesAutocomplete, {
  getGeocode,
  getLatLng,
} from "use-places-autocomplete";
import {
  Combobox,
  ComboboxInput,
  ComboboxPopover,
  ComboboxList,
  ComboboxOption,
} from "@reach/combobox";
import "@reach/combobox/styles.css";

type PlacesProps = {
  setStart: (position: google.maps.LatLngLiteral) => void;
  setEnd: (position: google.maps.LatLngLiteral) => void;
};

function Start({ setStart }: PlacesProps) {
  const {
    ready,
    value,
    setValue,
    suggestions: { status, data },
    clearSuggestions,
  } = usePlacesAutocomplete();

  const handleSelectStartpoint = async (val: string) => {
    setValue(val, false);
    clearSuggestions();

    const results = await getGeocode({ address: val });
    const { lat, lng } = await getLatLng(results[0]);
    setStart({ lat, lng });
  };

  return (
    <Combobox onSelect={handleSelectStartpoint}>
    <ComboboxInput
      value={value}
      onChange={(e) => setValue(e.target.value)}
      disabled={!ready}
      className="combobox-input"
      placeholder="Enter Startpoint"
    />
    <ComboboxPopover>
        <ComboboxList>
          {status === "OK" &&
            data.map(({ place_id, description }) => (
              <ComboboxOption key={place_id} value={description} />
            ))}
        </ComboboxList>
      </ComboboxPopover>
    </Combobox>
  );
}

function End({ setEnd }: PlacesProps) {
  const {
    ready,
    value,
    setValue,
    suggestions: { status, data },
    clearSuggestions,
  } = usePlacesAutocomplete();

  const handleSelectEndpoint = async (val: string) => {
    setValue(val, false);
    clearSuggestions();

    const results = await getGeocode({ address: val });
    const { lat, lng } = await getLatLng(results[0]);
    setEnd({ lat, lng });
  };

  return (
    <Combobox onSelect={handleSelectEndpoint}>
      <ComboboxInput
        value={value}
        onChange={(e) => setValue(e.target.value)}
        disabled={!ready}
        className="combobox-input"
        placeholder="Enter Endpoint"
      />
      <ComboboxPopover>
        <ComboboxList>
          {status === "OK" &&
            data.map(({ place_id, description }) => (
              <ComboboxOption key={place_id} value={description} />
            ))}
        </ComboboxList>
      </ComboboxPopover>
    </Combobox>
  );
}

export default function Places({ setStart, setEnd }: PlacesProps) {
  return (
    <>
      <Start setStart={setStart} />
      <End setEnd={setEnd} />
    </>
  );
}
